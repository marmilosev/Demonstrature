    private void btnDeleteActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnDeleteActionPerformed
        // TODO add your handling code here:
        if(lstPodaci.getSelectedValue() == null){
            JOptionPane.showMessageDialog(rootPane, "Odaberi stavku iz liste");
            return;
        }
        Mjesto m = lstPodaci.getSelectedValue();

        try {
            obrada.setEntitet(m);
            obrada.delete();
            ucitaj();
        } catch (Exception e) {
            JOptionPane.showMessageDialog(rootPane, e.getMessage());
        }
    }//GEN-LAST:event_btnDeleteActionPerformed

    private void btnUpdateActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnUpdateActionPerformed
        // TODO add your handling code here:
        if(lstPodaci.getSelectedValue() == null){
            JOptionPane.showMessageDialog(rootPane, "Odaberi stavku iz liste");
            return;
        }
        Mjesto m = lstPodaci.getSelectedValue();
        m.setNaziv(txtNaziv.getText());
        m.setPostanskiBroj(Integer.parseInt(txtPostBroj.getText()));

        try {
            obrada.setEntitet(m);
            obrada.update();
            ucitaj();
        } catch (Exception e) {
            JOptionPane.showMessageDialog(rootPane, e.getMessage());
        }
    }//GEN-LAST:event_btnUpdateActionPerformed

    private void btnCreateActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnCreateActionPerformed
        // TODO add your handling code here:
        Mjesto m = new Mjesto();
        m.setNaziv(txtNaziv.getText());
        m.setPostanskiBroj(Integer.parseInt(txtPostBroj.getText()));

        try {
            obrada.setEntitet(m);
            obrada.create();
            ucitaj();
        } catch (Exception e) {
            JOptionPane.showMessageDialog(rootPane, e.getMessage());
        }
    }//GEN-LAST:event_btnCreateActionPerformed

    private void lstPodaciValueChanged(javax.swing.event.ListSelectionEvent evt) {//GEN-FIRST:event_lstPodaciValueChanged
        // TODO add your handling code here:
        if(evt.getValueIsAdjusting() || lstPodaci.getSelectedValue() == null){
            return;
        }
        
        Mjesto m = lstPodaci.getSelectedValue();
        txtNaziv.setText(m.getNaziv());
        txtPostBroj.setText(Integer.toString(m.getPostanskiBroj()));
    }//GEN-LAST:event_lstPodaciValueChanged


