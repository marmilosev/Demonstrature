    private void lstPodaciValueChanged(javax.swing.event.ListSelectionEvent evt) {//GEN-FIRST:event_lstPodaciValueChanged
        // TODO add your handling code here:
        if(evt.getValueIsAdjusting() || lstPodaci.getSelectedValue() == null){
            return;
        }
        
        Zarucnica e = lstPodaci.getSelectedValue();
        txtBojaKose.setText(e.getBojaKose());
        txtJmbag.setText(e.getJmbag());
        cmbCura.setSelectedItem(e.getCura());
    }//GEN-LAST:event_lstPodaciValueChanged

    private void btnDodajActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnDodajActionPerformed
        // TODO add your handling code here:
        Zarucnica z = new Zarucnica();
        z.setBojaKose(txtBojaKose.getText());
        z.setJmbag(txtJmbag.getText());
        z.setCura((Cura) cmbCura.getSelectedItem());
        
        try {
            obrada.setEntitet(z);
            obrada.create();
            ucitaj();
        } catch (Exception e) {
            JOptionPane.showMessageDialog(rootPane, e.getMessage());
        }
    }//GEN-LAST:event_btnDodajActionPerformed

    private void btnPromijeniActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnPromijeniActionPerformed
        // TODO add your handling code here:
        Zarucnica z = lstPodaci.getSelectedValue();
         if (z == null) {
            JOptionPane.showMessageDialog(getRootPane(), "Prvo odaberite stavku");
            return;
        }
        
        z.setBojaKose(txtBojaKose.getText());
        z.setJmbag(txtJmbag.getText());
        z.setCura((Cura) cmbCura.getSelectedItem());
        
        try {
            obrada.setEntitet(z);
            obrada.create();
            ucitaj();
        } catch (Exception e) {
            JOptionPane.showMessageDialog(rootPane, e.getMessage());
        }
    }//GEN-LAST:event_btnPromijeniActionPerformed

    private void btnObrisiActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnObrisiActionPerformed
        // TODO add your handling code here:
        Zarucnica z = lstPodaci.getSelectedValue();
         if (z == null) {
            JOptionPane.showMessageDialog(getRootPane(), "Prvo odaberite stavku");
            return;
        }
        
        try {
            obrada.setEntitet(z);
            obrada.delete();
            ucitaj();
        } catch (Exception e) {
            JOptionPane.showMessageDialog(rootPane, e.getMessage());
        }
    }//GEN-LAST:event_btnObrisiActionPerformed


